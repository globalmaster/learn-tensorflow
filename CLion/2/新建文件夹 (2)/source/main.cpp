#include <QApplication>
#include <QTranslator>
#include <QMessageBox>
#include <QGLFormat>
#include "MainWindow.h"

int main( int argc, char** argv )
{
    QApplication app( argc, argv );
    QTranslator trans;

    trans.load( ":/Translation/zh_CN.qm" );
    app.installTranslator( &trans );

    if ( !( QGLFormat::openGLVersionFlags( ) &
            QGLFormat::OpenGL_Version_2_0 ) )
    {
        if ( QMessageBox::No ==
             QMessageBox::warning( 0,
                                   QApplication::tr( "Program information" ),
                                   QApplication::tr( "Sorry, the system may not support GLSL,\n"
                                                     "However, if you insist, the program will have a try,\n"
                                                     "but the result will be unpredictable." ),
                                   QMessageBox::Yes,
                                   QMessageBox::No ) )
        {
            qApp->quit( );
        }
    }

    MainWindow w;
    w.show( );

    return app.exec( );
}
